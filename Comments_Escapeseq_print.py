print("This is a string with double quotes")

#using escape sequence for new line
print("This is the first line.\nThis is the second line.")

#using escape sequence for single quote
print('It\'s a beautiful day!')

#using escape sequence to print double quotes inside a string
print("This is a string with \"double quotes\" inside it")

#using comma to print multiple items in one line
